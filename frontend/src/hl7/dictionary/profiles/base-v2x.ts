/* ------------------------------------------------------------------ helpers */

function f(
  seq: number,
  name: string,
  datatype: string,
  optionality?: Optionality,
  repeats?: boolean,
): FieldDef {
  return { seq, name, datatype, optionality, repeats };
}

function seg(id: string, name: string, fields: FieldDef[]): [string, SegmentDef] {
  const map: Record<number, FieldDef> = {};
  for (const field of fields) map[field.seq] = field;
  return [id, { id, name, fields: map }];
}

function dt(
  id: string,
  name: string,
  components?: Array<[number, string, string?]>,
): [string, DatatypeDef] {
  if (!components) return [id, { id, name }];
  const map: Record<number, { pos: number; name: string; datatype?: string }> = {};
  for (const [pos, cname, cdt] of components) map[pos] = { pos, name: cname, datatype: cdt };
  return [id, { id, name, components: map }];
}

